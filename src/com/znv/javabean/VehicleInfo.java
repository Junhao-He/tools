package com.znv.javabean;

public class VehicleInfo {
	private String index;
	private Integer type;
	private Integer colorDepth;
	private Integer color;
	private Integer speed;
	private Integer length;
	private Integer logoRecog;
	private Integer subLogoRecog;
	public String getIndex() {
		return index;
	}
	public void setIndex(String index) {
		this.index = index;
	}
	public Integer getType() {
		return type;
	}
	public void setType(Integer type) {
		this.type = type;
	}
	public Integer getColorDepth() {
		return colorDepth;
	}
	public void setColorDepth(Integer colorDepth) {
		this.colorDepth = colorDepth;
	}
	public Integer getColor() {
		return color;
	}
	public void setColor(Integer color) {
		this.color = color;
	}
	public Integer getSpeed() {
		return speed;
	}
	public void setSpeed(Integer speed) {
		this.speed = speed;
	}
	public Integer getLength() {
		return length;
	}
	public void setLength(Integer length) {
		this.length = length;
	}
	public Integer getLogoRecog() {
		return logoRecog;
	}
	public void setLogoRecog(Integer logoRecog) {
		this.logoRecog = logoRecog;
	}
	public Integer getSubLogoRecog() {
		return subLogoRecog;
	}
	public void setSubLogoRecog(Integer subLogoRecog) {
		this.subLogoRecog = subLogoRecog;
	}
	
}
