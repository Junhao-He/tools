package com.znv.javabean;

public class PlatInfo {
	private Integer type;
	private Integer color;
	private String license;
	private Integer licenseLen;
	private Integer entireBelieve;
	public Integer getType() {
		return type;
	}
	public void setType(Integer type) {
		this.type = type;
	}
	public Integer getColor() {
		return color;
	}
	public void setColor(Integer color) {
		this.color = color;
	}
	public String getLicense() {
		return license;
	}
	public void setLicense(String license) {
		this.license = license;
	}
	public Integer getLicenseLen() {
		return licenseLen;
	}
	public void setLicenseLen(Integer licenseLen) {
		this.licenseLen = licenseLen;
	}
	public Integer getEntireBelieve() {
		return entireBelieve;
	}
	public void setEntireBelieve(Integer entireBelieve) {
		this.entireBelieve = entireBelieve;
	}
	
}
